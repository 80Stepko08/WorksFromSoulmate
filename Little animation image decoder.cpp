CRGB Crgb332(byte a){
  CRGB col;
  col.r = a & 0xe0; // mask out the 3 bits of red at the start of the byte
  col.r |= (col.r >> 3); // extend limited 0-224 range to 0-252
  col.r |= (col.r >> 3); // extend limited 0-252 range to 0-255
  col.g = a & 0x1c; // mask out the 3 bits of green in the middle of the byte
  col.g |= (col.g << 3) | (col.r >> 3); // extend limited 0-34 range to 0-255
  col.b = a & 0x03; // mask out the 2 bits of blue at the end of the byte
  col.b |= col.b << 2; // extend 0-3 range to 0-15
  col.b |= col.b << 4; // extend 0-15 range to 0-255
  return col;
}

void draw() {
  static byte frame = 0;
  byte frameWidth = pgm_read_byte_near(&Data[0]);
  byte frameHeight = pgm_read_byte_near(&Data[1]);
  byte frames = pgm_read_byte_near(&Data[2]);
  byte steps = min(LED_COLS / frameWidth,LED_ROWS/frameHeight);
  byte *sprite_ptr = Data + 3 + frameWidth * frameHeight * frame;
  for (byte y = 0; y < frameHeight; y++) {
    for (byte x = 0; x < frameWidth; x++) {
      CRGB col = Crgb332(pgm_read_byte_near(sprite_ptr++));
      for (byte screenx = x * steps; screenx < x * steps + steps; screenx++)
        for (byte screeny = y * steps; screeny < y * steps + steps; screeny++)
          leds[XY(screenx, LED_COLS - 1 - screeny)] = col;
    }
  }
    frame++;
  if (frame >= frames) frame = 0;
  delay(50);
}